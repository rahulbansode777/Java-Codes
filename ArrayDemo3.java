class ArrayDemo3 {

	public static void main(String[] args) {

		int[] rahul = new int[5];
		float[] farr = new float[5];
		Double[] darr = new Double[5];

		System.out.println(rahul.getClass());	//[I
		System.out.println(farr.getClass());	//[F
		System.out.println(darr.getClass());
	}
}
