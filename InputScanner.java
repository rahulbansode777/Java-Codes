import java.util.*;

class InputScanner {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);

		int x = sc.nextInt();
		char y = sc.next().charAt(0);
		Float z = sc.nextFloat();

		System.out.println(x);
		System.out.println(y);
		System.out.println(z);

	}
}